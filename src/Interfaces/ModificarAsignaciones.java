    public void Modificar() throws SQLException {
        String id_curso = lbl_idcurso.getText();
        String id_docente = lbl_iddocente.getText();
        String id_turno = lbl_turno.getText();
        String id_semestre = lbl_semestre.getText();
        String id_grupo = lbl_grupo1.getText();

        pst = null;
        try {
            pst = conn.prepareStatement("UPDATE imparte SET id_docente='" + id_docente
                    + "',id_turno='" + id_turno + "',id_semestre='" + id_semestre + "',grupo='" + id_grupo + "' WHERE id_curso='" + id_curso + "'");
            int s = JOptionPane.showConfirmDialog(null, "¬øEstas seguro de modificar el registro?", "CONFIRMACION", 0);
            if (s == 0) {
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Modificacion exitosa, para ver los cambios, recargue el formulario");
                this.dispose();
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error " + e, "Error inesperado", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void MostrarDatosCurso() {
        String id = lbl_idcurso.getText();
        try {
            String sql = "select c.nombre as curso, d.id_docente as id, d.paterno as paterno, d.materno as materno, d.nombre as nombre,\n"
                    + "g.grupo as grupo, t.descripcion as turno, s.descripcion as semestre\n"
                    + "from docente d, imparte i, turno t, semestre s, grupos g, curso c\n"
                    + "where c.id_curso='" + id + "'\n"
                    + "and i.id_curso=c.id_curso and d.id_docente=i.id_docente\n"
                    + "and i.id_turno=t.id_turno and i.id_semestre=s.id_semestre\n"
                    + "and i.grupo=g.id_grupo";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                lbl_asignatura.setText(rs.getString("curso"));
                lbl_id_docente.setText(rs.getString("id"));
                lbl_paterno.setText(rs.getString("paterno"));
                lbl_materno.setText(rs.getString("materno"));
                lbl_nombre.setText(rs.getString("nombre"));
                lbl_grupo.setText(rs.getString("grupo"));
                lbl_dateturno.setText(rs.getString("turno"));
                lbl_datesemestre.setText(rs.getString("semestre"));
            }
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "No se cargaron los datos " + e.getMessage(), "Error inesperado", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void MostrarDocentes() {
        DefaultTableModel modelo_docentes = new DefaultTableModel();
        modelo_docentes.addColumn("Id");
        modelo_docentes.addColumn("Paterno");
        modelo_docentes.addColumn("Materno");
        modelo_docentes.addColumn("Nombre");
        tbl_docentes.setModel(modelo_docentes);

        String datos[] = new String[4];
        try {
            String sql = "SELECT id_docente, paterno, materno, nombre FROM docente ORDER BY id_docente ASC";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                modelo_docentes.addRow(datos);
            }
            tbl_docentes.setModel(modelo_docentes);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar docentes", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

